const accentWords = ['Automation', 'Automatisierung', 'Автоматизация', '自动化', 'Automatyzacja', 'أتمتة', 'Automatisation', 'Uathoibriú'];

const withPremiumAccent = (title) => {
  const word = accentWords.find((candidate) => title.includes(candidate));
  return word ? title.replace(word, `<span>${word}</span>`) : title;
};

const applyPremiumLanguageTranslation = () => {
  const language = document.body?.dataset?.language;
  const copy = premiumLanguageTranslations[language];
  if (!copy) return;

  document.querySelector('[data-premium-copy="manifestoKicker"]')?.replaceChildren(document.createTextNode(copy.manifestoKicker));

  const title = document.querySelector('[data-premium-copy="manifestoTitle"]');
  if (title) title.innerHTML = withPremiumAccent(copy.manifestoTitle);

  document.querySelector('[data-premium-copy="manifestoBody"]')?.replaceChildren(document.createTextNode(copy.manifestoBody));
  document.querySelector('[data-premium-copy="capabilitiesKicker"]')?.replaceChildren(document.createTextNode(copy.capabilitiesKicker));
  document.querySelector('[data-premium-copy="capabilitiesTitle"]')?.replaceChildren(document.createTextNode(copy.capabilitiesTitle));

  const rail = document.querySelector('[data-premium-rail]');
  if (rail) rail.innerHTML = copy.rail.map((item) => `<span>${item}</span>`).join('');

  const capabilities = document.querySelector('[data-premium-capabilities]');
  if (capabilities) {
    capabilities.innerHTML = copy.capabilities.map(([capabilityTitle, body], index) => `
      <article class="capability-item premium-reveal is-visible" style="--premium-delay:${index * 70}ms">
        <span class="capability-item__index">${String(index + 1).padStart(2, '0')}</span>
        <div>
          <h4>${capabilityTitle}</h4>
          <p>${body}</p>
        </div>
      </article>
    `).join('');
  }

  Object.entries(copy.terminal).forEach(([key, value]) => {
    document.querySelector(`[data-premium-terminal="${key}"]`)?.replaceChildren(document.createTextNode(value));
  });
};

const initPremiumLanguageTranslations = () => {
  applyPremiumLanguageTranslation();

  const observer = new MutationObserver(() => {
    window.requestAnimationFrame(applyPremiumLanguageTranslation);
  });

  observer.observe(document.body, {
    attributes: true,
    childList: true,
    subtree: true,
    attributeFilter: ['data-language'],
  });

  document.addEventListener('click', () => {
    window.setTimeout(applyPremiumLanguageTranslation, 120);
  });
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initPremiumLanguageTranslations);
} else {
  initPremiumLanguageTranslations();
}
